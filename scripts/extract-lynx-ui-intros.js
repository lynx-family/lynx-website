const fs = require('fs');
const path = require('path');

const resolveLynxUiPackagesDir = () => {
  const candidates = ['@lynx-js/lynx-ui-repo'];
  for (const candidate of candidates) {
    try {
      const pkgJsonPath = require.resolve(`${candidate}/package.json`);
      const repoRoot = path.dirname(pkgJsonPath);
      const packagesDir = path.join(repoRoot, 'packages');
      if (fs.existsSync(packagesDir)) {
        return packagesDir;
      }
    } catch {
      continue;
    }
  }
  return null;
};

const packagesDir = resolveLynxUiPackagesDir();
/** @type {Record<string, string>} */
const result = {};

if (!packagesDir || !fs.existsSync(packagesDir)) {
  console.warn(
    'Packages directory not found for lynx-ui. Skipping intros extraction.',
  );
  process.exit(0);
}

const packages = fs.readdirSync(packagesDir);

for (const pkg of packages) {
  const readmePath = path.join(packagesDir, pkg, 'README.md');
  if (fs.existsSync(readmePath)) {
    const content = fs.readFileSync(readmePath, 'utf8');
    const lines = content.split('\n');

    let foundHeader = false;
    let intro = '';

    for (const line of lines) {
      if (line.startsWith('# ')) {
        foundHeader = true;
        continue;
      }
      if (foundHeader && line.trim().length > 0) {
        intro = line.trim();
        break;
      }
    }

    if (intro) {
      result[pkg] = intro;
    }
  }
}

const outputPath = path.resolve(__dirname, '../sharedDocs/lynx-ui-intros.ts');

if (fs.existsSync(outputPath)) fs.unlinkSync(outputPath);

const sortedEntries = Object.entries(result).sort(([a], [b]) =>
  a.localeCompare(b),
);
const sortedResult = Object.fromEntries(sortedEntries);

const fileContent =
  `// This file is auto-generated by scripts/extract-lynx-ui-intros.js\n` +
  `// Do not edit manually.\n` +
  `\n` +
  `const lynxUiIntros: Record<string, string> = ${JSON.stringify(
    sortedResult,
    null,
    2,
  )};\n` +
  `\n` +
  `export default lynxUiIntros;\n`;

fs.writeFileSync(outputPath, fileContent, 'utf8');

console.log(
  'Successfully extracted introductions to sharedDocs/lynx-ui-intros.ts!',
);
